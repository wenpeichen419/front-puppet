        }, 100);

        this.$message.success(`‰∏ãËΩΩÊàêÂäüÔºö${resourceInfo.filename || item.title}`);
      } catch (error) {
        console.error('‰∏ãËΩΩÂ§±Ë¥•:', error)
        this.$message.error(`‰∏ãËΩΩÂ§±Ë¥•: ${error.message}`)
      } finally {
        this.isDownloading = false;
      }
    },

    shareResource(item) {
      if (navigator.share) {
        navigator.share({
          title: item.title,
          text: item.description,
          url: window.location.href
        }).then(() => {
          this.$message.success('ÂàÜ‰∫´ÊàêÂäüÔºÅ');
